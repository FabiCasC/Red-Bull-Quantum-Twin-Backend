def get_db_status() -> str:
    """Return active database engine: 'oracle' or 'sqlite_fallback'."""
    _resolve_backend()
    return _active_backend or "sqlite_fallback"


def _oracle_configured() -> bool:
    required = ("ORACLE_USER", "ORACLE_PASSWORD", "ORACLE_DSN", "ORACLE_WALLET_DIR")
    return all(os.getenv(key) for key in required) and oracledb is not None


def _wallet_dir() -> str:
    wallet = os.getenv("ORACLE_WALLET_DIR", "wallet")
    if not os.path.isabs(wallet):
        wallet = os.path.join(os.path.dirname(__file__), wallet)
    return wallet


def _connect_oracle():
    wallet_dir = _wallet_dir()
    if not os.path.isdir(wallet_dir):
        raise FileNotFoundError(f"Oracle wallet directory not found: {wallet_dir}")

    kwargs = {
        "user": os.environ["ORACLE_USER"],
        "password": os.environ["ORACLE_PASSWORD"],
        "dsn": os.environ["ORACLE_DSN"],
        "config_dir": wallet_dir,
        "wallet_location": wallet_dir,
    }
    wallet_password = os.getenv("ORACLE_WALLET_PASSWORD")
    if wallet_password:
        kwargs["wallet_password"] = wallet_password

    return oracledb.connect(**kwargs)


def _connect_sqlite():
    os.makedirs(os.path.dirname(DB_PATH) or ".", exist_ok=True)
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def _resolve_backend():
    global _active_backend
    if _active_backend is not None:
        return

    if not _oracle_configured():
        if oracledb is None:
            print(
                "WARNING: oracledb not installed or Oracle env vars missing. "
                "Using SQLite fallback at db/quantum_twin.db"
            )
        else:
            print(
                "WARNING: Oracle credentials/wallet not configured. "
                "Using SQLite fallback at db/quantum_twin.db"
            )
        _active_backend = "sqlite_fallback"
        return

    try:
        conn = _connect_oracle()
        conn.close()
        _active_backend = "oracle"
        print(f"Oracle Autonomous Database connected (DSN: {os.environ['ORACLE_DSN']})")
    except Exception as exc:
        print(
            f"WARNING: Oracle connection failed ({exc}). "
            "Using SQLite fallback at db/quantum_twin.db"
        )
        _active_backend = "sqlite_fallback"


def get_connection():
    _resolve_backend()
    if _active_backend == "oracle":
        return _connect_oracle()
    return _connect_sqlite()


def _init_oracle(conn):
    cursor = conn.cursor()
    for ddl in ORACLE_TABLES:
        try:
            # Oracle Autonomous Database (OCI) in production
            cursor.execute(ddl)
        except oracledb.DatabaseError as exc:
            if exc.args[0].code != 955:  # ORA-00955: name already used
                raise
    conn.commit()
    cursor.close()


def _init_sqlite(conn):
    # Oracle Autonomous Database (OCI) in production
    conn.executescript(SQLITE_INIT_SCRIPT)
    conn.commit()


def init_db():
    conn = get_connection()
    try:
        if _active_backend == "oracle":
            _init_oracle(conn)
        else:
            _init_sqlite(conn)
    finally:
        conn.close()


