def _rows_to_dicts(cursor, rows) -> list[dict]:
    columns = [col[0].lower() for col in cursor.description]
    result = []
    for row in rows:
        item = {}
        for col, val in zip(columns, row):
            if hasattr(val, "isoformat"):
                val = val.isoformat()
            item[col] = val
        result.append(item)
    return result


def get_recent_predictions(limit=10):
    conn = get_connection()
    try:
        if _active_backend == "oracle":
            cursor = conn.cursor()
            # Oracle Autonomous Database (OCI) in production
            cursor.execute(
                """
                SELECT * FROM superclipping_predictions
                ORDER BY "timestamp" DESC
                FETCH FIRST :lim ROWS ONLY
                """,
                lim=limit,
            )
            rows = cursor.fetchall()
            result = _rows_to_dicts(cursor, rows)
            cursor.close()
            return result

        # Oracle Autonomous Database (OCI) in production
        rows = conn.execute(
            "SELECT * FROM superclipping_predictions ORDER BY timestamp DESC LIMIT ?",
            (limit,),
        ).fetchall()
        return [dict(row) for row in rows]
    finally:
        conn.close()


